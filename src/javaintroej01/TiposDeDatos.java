public class TiposDeDatos {


    public static void main(String[] args) {

        // Declarar variables de diferentes tipos de datos
        int numeroEntero = 10; // integer
        double numeroConDecIMAL = 3.14; // double
        boolean esVerdadero = true; // boolean
        char caracter = 'A'; // character
        String cadenaDeTexto = "Hola, mundo!"; // string
        byte byteValor = 127; // byte

        System.out.println("Variables de diferentes tipos de datos:");
        System.out.println("Numero entero: " + numeroEntero);
        System.out.println("Numero con decimal: " + numeroConDecIMAL);
        System.out.println("Es verdadero: " + esVerdadero);
        System.out.println("Caracter: " + caracter);
        System.out.println("Cadena de texto: " + cadenaDeTexto);
        System.out.println("Byte valor: " + byteValor);
    }
}